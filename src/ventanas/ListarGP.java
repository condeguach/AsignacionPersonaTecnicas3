       public void setListaGrafica() { //metodo para listar los usuarios creamos el objeto lista
        if (jlisListaUsu==null) {      //luego cargamos los datos en una variable y poder a√±adirlos en el orden deseado
            return;                    // a la variable instanciada de lista default y finalmente a el objeto lista
        }

        miLista = new DefaultListModel();
        for (Persona persona : listaPersona) {
            String datos=("Nombre:"+persona.getNombre()+" Cedula:"+persona.getNacionalidad()+persona.getCedula()+" EDAD:"+persona.getEdad());
            miLista.addElement(datos);
            
        }

        jlisListaUsu.setModel(miLista);
    }
    
    
    
