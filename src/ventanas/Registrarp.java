    private void tfNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfNombreKeyTyped
        // TODO add your handling code here:
       //validamos solo texto en nombre
        char tecla;
        tecla = evt.getKeyChar();
        if (!Character.isLetter(tecla) && tecla != KeyEvent.VK_SPACE && tecla != KeyEvent.VK_BACKSPACE) {
            evt.consume();
            getToolkit().beep();
            JOptionPane.showMessageDialog(null,"SOLO PUEDE INTRODUCIR LETRAS");

        }

    }//GEN-LAST:event_tfNombreKeyTyped

    private void tfCedulaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCedulaKeyTyped
        // TODO add your handling code here:
        //validamos solo numeros en cedula
        char tecla;
        tecla = evt.getKeyChar();
        if (!Character.isDigit(tecla) && tecla != KeyEvent.VK_BACKSPACE) {
            evt.consume();
            getToolkit().beep();
            JOptionPane.showMessageDialog(null,"SOLO PUEDE INTRODUCIR NUMEROS");
        }

    }//GEN-LAST:event_tfCedulaKeyTyped

